// Single-line Comments in C 

// Note: In C, any text after '//' on the same line is ignored by the compiler.
// That is why these are called "single-line" or "inline" comments.

#include <stdio.h>

int main() {
  
  // (This is a single-line comment) create integer variable    
  int age = 25; 
 
  printf("Age: %d", age);   // (This is also a single-line comment) print the age variable

  return 0;
}
