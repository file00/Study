/* Assignment Operators */
// Working of assignment operators
#include <iostream>
using namespace std;

int main() {
    int a = 5, c;
    
    c = a;
    cout << "c = " << c << endl;
    c += a;
    cout << "c = " << c << endl;
    c -= a;
    cout << "c = " << c << endl;
    c *= a;
    cout << "c = " << c << endl;
    c /= a;
    cout << "c = " << c << endl;
    c %= a;
    cout << "c = " << c << endl;

    return 0;
}



   

