// String Literals

#include <stdio.h>

int main() {
    
    // Display "good"
    printf("\"good\"                  //string constant\n");

    // Display ""
    printf("\"\"                     //null string constant\n");

    // Display "      "
    printf("\"      \"               //string constant of six white space\n");

    // Display "x"
    printf("\"x\"                    //string constant having a single character.\n");

    // Display "Earth is round\n"
    printf("\"Earth is round\\n\"         //prints string with a newline\n");

    return 0;
}
