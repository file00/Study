// Characters

#include <stdio.h>

int main() {
    
    // Define character constants
    char a = 'a';
    char m = 'm';
    char f = 'F';
    char two = '2';
    char brace = '}';

    // Print the descriptive text using character literals
    printf("A character literal is created by enclosing a single character inside single quotation marks. \n");
    printf("For example: '%c', '%c', '%c', '%c', '%c' etc.\n", a, m, f, two, brace);

    return 0;
}