/* Expressing Square Roots and Roots */
#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double a = 25;
    double b = 100;
    
    cout << endl;
    cout << "Square Roots" << endl;
    cout << "10 ^ 3 = " << pow(10, 3) << endl;
    cout << "4 ^ 7 = " << pow(4, 7) << endl;
    cout << endl;
    cout << "Roots" << endl;
    cout << "sqrt(25) = " << sqrt(a) << endl;
    cout << "sqrt(100) = " << sqrt(b) << endl;
    cout << endl;
    
    return 0;
}

