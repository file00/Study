/* Predefined Macros */
using namespace std;
#include <iostream>

int main() {

    cout << "Current date: " << __DATE__ << endl;
    cout << "Current file: " << __FILE__ << endl;
    cout << "Current line: " << __LINE__ << endl;
    cout << "Current stdc: " << __STDC__ << endl;
    cout << "Current time: " << __TIME__ << endl;
   
    return 0;
}
