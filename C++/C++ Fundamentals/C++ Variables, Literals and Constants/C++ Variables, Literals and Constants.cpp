/* C++ Variables, Literals and Constants */

#include <iostream>
using namespace std;

int main() {
    // C++ Variables
    int age = 14;   // age is 14
    cout << age << endl;
    age = 17;       // age is 17
    cout << age << endl;
    
    // C++ Constants
    const int LIGHT_SPEED = 299792458;
    cout << LIGHT_SPEED << endl;
    // LIGHT_SPEED = 2500; // Error! LIGHT_SPEED is a constant.
    // cout << LIGHT_SPEED << endl;

    return 0;
}