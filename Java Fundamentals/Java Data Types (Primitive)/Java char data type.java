/* Java char data type */
class Main {
  public static void main(String[] args) {
    	
    char letter = '\u0051';
    char letter1 = '9';
    char letter2 = 65;
    
    System.out.println(letter);  // prints Q
    System.out.println(letter1);  // prints 9
    System.out.println(letter2);  // prints A
  }
}